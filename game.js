function update() {
    // Implement game logic (movement, collisions, etc.)
    if (cursors.left.isDown) {
        pacman.setVelocityX(-160);
        pacman.setVelocityY(0);
        pacman.anims.play('left', true);
    } else if (cursors.right.isDown) {
        pacman.setVelocityX(160);
        pacman.setVelocityY(0);
        pacman.anims.play('right', true);
    } else if (cursors.up.isDown) {
        pacman.setVelocityY(-160);
        pacman.setVelocityX(0);
        pacman.anims.play('up', true);
    } else if (cursors.down.isDown) {
        pacman.setVelocityY(160);
        pacman.setVelocityX(0);
        pacman.anims.play('down', true);
    } else {
        pacman.setVelocityX(0); // Stop when no key is pressed
        pacman.setVelocityY(0);
    }
}



